const MORSE_TABLE = {
    '.-': 'a',
    '-...': 'b',
    '-.-.': 'c',
    '-..': 'd',
    '.': 'e',
    '..-.': 'f',
    '--.': 'g',
    '....': 'h',
    '..': 'i',
    '.---': 'j',
    '-.-': 'k',
    '.-..': 'l',
    '--': 'm',
    '-.': 'n',
    '---': 'o',
    '.--.': 'p',
    '--.-': 'q',
    '.-.': 'r',
    '...': 's',
    '-': 't',
    '..-': 'u',
    '...-': 'v',
    '.--': 'w',
    '-..-': 'x',
    '-.--': 'y',
    '--..': 'z',
    '.----': '1',
    '..---': '2',
    '...--': '3',
    '....-': '4',
    '.....': '5',
    '-....': '6',
    '--...': '7',
    '---..': '8',
    '----.': '9',
    '-----': '0',
};


function decode(expr) {
    let array = [...(expr.match(/.{1,10}/g))];
    for (let justCnt = 0; justCnt < array.length; justCnt++) {

        if (array[justCnt] === "**********") array[justCnt] = " ";

        else array[justCnt] = MORSE_TABLE[array[justCnt].replace(/^0*/, "").replace(/10/g, ".").replace(/11/g, "-")];

    }

    return array.join("");
}

module.exports = {
    decode
}